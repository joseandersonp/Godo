package godo.scene;

public class CameraPlacement {

	private int[] positionX = new int[3]; 
	private int[] positionY= new int[3]; 
	private int[] positionZ = new int[3];
	private int[] directionX= new int[3];
	private int[] directionY= new int[3];
	private int[] directionZ= new int[3];
	
	public int[] getPositionX() {
		return positionX;
	}
	public void setPositionX(int[] positionX) {
		this.positionX = positionX;
	}
	public int[] getPositionY() {
		return positionY;
	}
	public void setPositionY(int[] positionY) {
		this.positionY = positionY;
	}
	public int[] getPositionZ() {
		return positionZ;
	}
	public void setPositionZ(int[] positionZ) {
		this.positionZ = positionZ;
	}
	public int[] getDirectionX() {
		return directionX;
	}
	public void setDirectionX(int[] directionX) {
		this.directionX = directionX;
	}
	public int[] getDirectionY() {
		return directionY;
	}
	public void setDirectionY(int[] directionY) {
		this.directionY = directionY;
	}
	public int[] getDirectionZ() {
		return directionZ;
	}
	public void setDirectionZ(int[] directionZ) {
		this.directionZ = directionZ;	}
	

}
